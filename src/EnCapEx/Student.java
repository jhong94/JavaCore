package EnCapEx;

public class Student {
    private String name;
    private int age;
    private String grade;

    public String getName(){
        return name;
    }

    public int getAge(){
        return age;
    }

    public String getGrade(){
        return grade;
    }

    public void setName(String newName){
        name = newName;
    }

    public void setAge(int newAge){
        age = newAge;
    }

    public void setGrade(String newGrade){
        grade = newGrade;
    }
}
